#include "../push_swap.h"

static void	push(t_stack **src, t_stack **dst)
{
	t_stack	*node;

	node = *src;
	*src = (*src)->next;
	if (*src)
	{
		(*src)->prev = NULL;
	}
	node->prev = NULL;
	if (*dst == NULL)
	{
		*dst = node;
		node->next = NULL;
	}
	else
	{
		node->next = *dst;
		node->next->prev = node;
		*dst = node;
	}
}

void	pa(t_stack **a, t_stack **b)
{
	push(b, a);
	ft_printf("pa\n");
}

void	pb(t_stack **a, t_stack **b)
{
	push(a, b);
	ft_printf("pb\n");
}
